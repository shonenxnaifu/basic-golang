package main

import "fmt"

func main() {
  // number
  fmt.Println("Satu = ", 1)
  fmt.Println("Dua = ", 2)
  fmt.Println("Tiga Koma Lima = ", 3.5)
  
  // boolean
  fmt.Println("Benar = ", true)
  fmt.Println("Salah = ", false)

  // string
  fmt.Println("Eko")
  fmt.Println("Eko Kurniawan")
  fmt.Println("Eko Kurniawan Khannedy")

  fmt.Println(len("eko"))
  fmt.Println("Eko Kurniawan"[0])
  fmt.Println("Eko Kurniawan Khannedy"[1])

}
